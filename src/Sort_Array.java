import java.util.Arrays;

public class Sort_Array
{
    public static void main(String args[])
    {
        int a[]={10,20,70,90,50,60}; // array intialisation n declaration
        Arrays.sort(a);
        System.out.println("sorting numeric array ascending order :"+Arrays.toString(a));

        String b[]={"hetal","chirag","nishu","mummy"};// intialisation and declaration
        Arrays.sort(b);
        System.out.println("sorting string array ascending order =  "+Arrays.toString(b));

    }




}

